import java.util.HashSet;
import java.util.Random;
import java.util.Set;

public class Host extends User {
    public Host(int money, int ID) {
        super(money, ID);
    }

    public Set<Integer> send(int num){
        System.out.println("群主余额："+this.getMoney());

        int money = new Random().nextInt(this.getMoney())/num;
        Set<Integer> set = new HashSet<>();
        for (int i = 0; i < num; i++) {
            set.add(money);
            this.setMoney(this.getMoney()-money);
        }

        System.out.println("群主剩余："+this.getMoney());

        return set;
    }
}

