package com.zrx.domain.assist;

import java.util.List;

public class Ids {
    List<Integer> ids;

    public List<Integer> getIds() {
        return ids;
    }

    public void setIds(List<Integer> ids) {
        this.ids = ids;
    }

    @Override
    public String toString() {
        return "Ids{" +
                "ids=" + ids +
                '}';
    }
}
